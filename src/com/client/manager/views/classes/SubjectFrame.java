    private void initData() {
        // reset
        mSubjects.removeAllElements();
        new Thread(() -> {
            LoadingScreen load = new LoadingScreen("Xin chờ...");
            load.setVisible(true);
            // load
            List<Subject> subjects = WebMethods.getSubjects();
            for (Subject s : subjects) {
                mSubjects.addElement(new SubjectDTO(s));
            }
            // end
            load.dispose();
        }).start();
        resetForm();
    }

    private void resetForm() {
        txtName.setText("");
        mInfo.setRowCount(0);
        btnRemoveInfo.setEnabled(false);
    }
