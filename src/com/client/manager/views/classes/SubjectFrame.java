    private void btnAddSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSubjectActionPerformed
        // TODO add your handling code here:
        String name = WindowUtility.showInputPrompt(this, "Thêm môn học", "Nhập tên môn học muốn thêm:");
        if (name != null) {
            Subject s = new Subject();
            s.setName(name.trim());
            s.setInfo("");
            new Thread(() -> {
                int result = WebMethods.addSubject(s);
                if (result <= 0) {
                    WindowUtility.showMessage(this, "Thêm môn học - Lỗi", "Không thể thêm môn học.\nVui lòng thử lại!", WindowUtility.ERROR);
                }
                initData();
            }).start();
        }
    }//GEN-LAST:event_btnAddSubjectActionPerformed

