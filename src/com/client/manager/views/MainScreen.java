    private void chkWebActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkWebActionPerformed
        // TODO add your handling code here:
        if (chkWeb.isSelected()) {
            // User wants to check
            ConfirmOption conf = WindowUtility.showConfirm(jMenu1, "Vô hiệu hóa website xem điểm", "Phụ huynh và học sinh sẽ không thể sử dụng website xem điểm.\nBạn có chắc chắn?");
            if (conf.equals(ConfirmOption.YES)) {
                Properties p = new Properties();
                p.setKey("web_status");
                p.setValue("0");
                new Thread(() -> {
                    WebMethods.updateProperty(p);
                }).start();
            } else {
                chkWeb.setSelected(false);
            }
        } else {
            // User wants to uncheck
            Properties p = new Properties();
            p.setKey("web_status");
            p.setValue("1");
            new Thread(() -> {
                WebMethods.updateProperty(p);
            }).start();
        }
    }//GEN-LAST:event_chkWebActionPerformed

    private void chkAppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkAppActionPerformed
        // TODO add your handling code here:
        if (chkApp.isSelected()) {
            // User wants to check
            ConfirmOption conf = WindowUtility.showConfirm(jMenu1, "Vô hiệu hóa ứng dụng giáo viên", "Giáo viên sẽ khoogn thể đăng nhập ứng dụng nhập điểm.\nBạn có chắc chắn?");
            if (conf.equals(ConfirmOption.YES)) {
                Properties p = new Properties();
                p.setKey("app_status");
                p.setValue("0");
                new Thread(() -> {
                    WebMethods.updateProperty(p);
                }).start();
            } else {
                chkApp.setSelected(false);
            }
        } else {
            // User wants to uncheck
            Properties p = new Properties();
            p.setKey("app_status");
            p.setValue("1");
            new Thread(() -> {
                WebMethods.updateProperty(p);
            }).start();
        }
    }//GEN-LAST:event_chkAppActionPerformed

